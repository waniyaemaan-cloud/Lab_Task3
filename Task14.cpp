#include<iostream>
using namespace std;
main(){
    int n=5;
    for(int i=1;i<=n;i=i+1){
    for(int j=1;j<=i;j=j+1){
        cout<<"*";
    }
    cout<<endl;
}
    for(int i=n;i>=1;i=i-1){
        for(int j=1;j<=i;j=j+1){
            cout<<"*";
        }
        cout<<endl;
    }
    }
